#include <iostream>


struct A { 	// оголошення класу
  static int i;
  static int f(int j) { i = j; return i + j; }
};


int A::i = 3;  	// визначення статичного члена


int main() {
  // оголошення вказівника на статичний
  // член даних класу

    int *p1 = &A::i;
    // присвоєння значення через вказівник
    *p1 = 1;
    // оголошення вказівника на статичний
    // метод класу
    int(*p2)(int) = &A::f;
    // виклик функції через вказівник
    (*p2)(3);
    A a;
    std::cout << a.i;
  }
